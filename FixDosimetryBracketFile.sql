--- ANALYSIS BASED ON WO

DECLARE @WO varchar(50)
SET @WO = 'C5B000072'

SELECT DISTINCT 
	po.ProductionOrderNumber 
	,u.ID UnitID 
	,'''' + sn.Value UnitSN 
	,SUBSTRING(ud.Reserved_14,1,12) TraySN 
	,u.StatusID 
	,us.Description UnitStatus 
	,u.UnitStateID ,ud.reserved_24 BeagleDoesResult
	,ud.reserved_25 PelletFirst 
	,ud.reserved_26 PelletLast
	,ptrf.DoseMin DoseMinFirst 
	,ptrf.DoseMax DoseMaxFirst 
	,ptrf.FinalResult FinalResultFirst 
	,ptrf.BracketNumber BracketNumberFirst
	,ptrl.DoseMin DoseMinLast 
	,ptrl.DoseMax DoseMaxLast 
	,ptrl.FinalResult FinalResultLast 
	,ptrl.BracketNumber BracketNumberLast
	,h.ExitTime BeagleTestExitTime 
	,h.UnitStateID 
	,h.StationID
FROM ffUnit u WITH(nolock) 
INNER JOIN ffProductionOrder po WITH(nolock) On u.ProductionOrderID = po.ID
INNER JOIN ffUnitDetail ud WITH(nolock) ON u.ID = ud.UnitID
INNER JOIN luUnitStatus us WITH(nolock) ON u.StatusID = us.ID
INNER JOIN ffSerialNumber sn WITH(nolock) ON u.Id = sn.UnitID and sn.SerialNumberTypeID = 0
LEFT JOIN udtPelletTrayAssign ptaf WITH(nolock) ON ud.reserved_25 = ptaf.PelletSN
LEFT JOIN udtPelletTrayAssign ptal WITH(nolock) ON ud.reserved_26 = ptal.PelletSN
LEFT JOIN udtTrayPelletTestResults ptrf WITH(nolock) ON ptaf.ID = ptrf.TrayPelletID
LEFT JOIN udtTrayPelletTestResults ptrl WITH(nolock) ON ptal.ID = ptrl.TrayPelletID
LEFT JOIN 
	(
		SELECT h.UnitID ,h.UnitStateID ,h.StationID ,st.Description Station ,h.ExitTime FROM ffHistory h WITH(nolock)
		INNER JOIN ffProductionOrder po WITH(nolock) ON h.ProductionOrderID = po.ID
		INNER JOIN ffStation st WITH(nolock) ON h.StationID = st.ID
		WHERE po.ProductionOrderNumber = @WO AND st.Description = 'Beagle_L01'
	) h ON u.ID = h.UnitID
WHERE po.ProductionOrderNumber = @WO

